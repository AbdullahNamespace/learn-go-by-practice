package main

import "fmt"

// ======================
//     OUTPUT FUNCTIONS
// ======================

func printLettersAtoZ() {
	for i := 65; i <= 90; i++ {
		fmt.Println(string(i))
	}
}

// ======================
//         MAIN
// ======================

func main() {
	printLettersAtoZ()
}
