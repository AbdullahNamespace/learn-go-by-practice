package main

import "fmt"

// ======================
//     OUTPUT FUNCTIONS
// ======================

func printAllFromAAAtoZZZ() {
	for i := 65; i <= 90; i++ {
		for j := 65; j <= 90; j++ {
			for k := 65; k <= 90; k++ {
				fmt.Printf("%c%c%c\n", i, j, k)
			}
		}
		fmt.Println("____________________________")
	}
}

// ======================
//         MAIN
// ======================

func main() {
	printAllFromAAAtoZZZ()
}
