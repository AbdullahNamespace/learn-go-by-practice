package main

import (
	"fmt"
	"math/rand/v2"
)

// ======================
//   PROCESSING FUNCTIONS
// ======================

func randomNumber(from, to int) int {
	return rand.IntN(to-from+1) + from
}

// ======================
//     OUTPUT FUNCTIONS
// ======================

func printResult() {
	fmt.Println(randomNumber(1, 10))
	fmt.Println(randomNumber(1, 10))
	fmt.Println(randomNumber(1, 10))
}

// ======================
//         MAIN
// ======================

func main() {
	printResult()
}
